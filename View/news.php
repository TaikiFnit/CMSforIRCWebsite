      <h3>BCN ITジュニア賞 2015を受賞しました。</h3>
      <p>
        <time datetime="2015-01-22">2015年1月22日</time>
      </p>
      <p id="newsText"> 015年01月16日に開催された「BCN ITジュニア賞 2015」の表彰式に参加しました。
        今年度は、全国高校生プログラミングコンテストに優勝した5人のメンバーが表彰されました。
        情報研究部でこの賞をいただくのは6年連続になります。
        このように連続して賞をいただくことができたのは先生方の熱心なご指導があってのこと、先輩方が積み上げてきた伝統があってのこと、さらには普段からご支援いただいている多くの皆様のおけげだと思っております。
        来年度もこのような名誉ある賞をいただけるようにこれからも精一杯活動に励みたいと思います。
        応援よろしくお願いします。 </p>
